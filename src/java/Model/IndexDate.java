package Model;

public class IndexDate {

    private String dateStampExcelFormat;
    private String dayOfWeek;

    public String getDateStampExcelFormat() {
        return dateStampExcelFormat;
    }

    public void setDateStampExcelFormat(String dateStampExcelFormat) {
        this.dateStampExcelFormat = dateStampExcelFormat;
    }

    public String getDayOfWeek() {
        return dayOfWeek;
    }

    public void setDayOfWeek(String dayOfWeek) {
        this.dayOfWeek = dayOfWeek;
    }

}
